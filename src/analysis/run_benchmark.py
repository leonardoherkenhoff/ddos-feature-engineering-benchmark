import pandas as pd
import numpy as np
import os
import gc
import warnings
from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import train_test_split
from sklearn.metrics import f1_score, precision_score, recall_score
from sklearn.preprocessing import LabelEncoder
warnings.simplefilter(action='ignore', category=FutureWarning)
"""
Unified Tripartite DDoS Extractor Benchmark
Evaluates CICFlowMeter, NTLFlowLyzer, and ALFlowLyzer features.
Implements standardized Data Engineering techniques (Label Encoding for L7 features)
to guarantee fair Top 10 Feature extraction across all tools.
"""
# --- CONFIGURATION (Aligned with GitHub Repo Structure) ---
DIRS = {
    'CIC': './data/processed/CIC',
    'NTL': './data/processed/NTL',
    'AL':  './data/processed/AL'
}
OUTPUT_DIR = "./results/figures"
ATTACK_KEYWORDS = ['DNS', 'LDAP', 'MSSQL', 'NetBIOS', 'NTP', 'SNMP', 'SSDP', 'UDP', 'Syn', 'TFTP', 'UDPLag', 'Portmap'] 
SAFE_THRESHOLD = 500 * 1024 * 1024 # Limit to trigger reservoir sampling
CHUNK_SIZE = 200_000 
MAX_ROWS_PER_FILE = 2_500_000 
if not os.path.exists(OUTPUT_DIR):
    os.makedirs(OUTPUT_DIR)
def process_chunk(df_chunk, is_al=False):
    """Sanitizes chunk data, removing identifying metadata and handling anomalies."""
    df_chunk.columns = [c.strip().lower().replace(' ', '_') for c in df_chunk.columns]
    possible_labels = [c for c in df_chunk.columns if 'label' in c or 'class' in c]
    if not possible_labels: return None, None
    target_col = possible_labels[-1]
    # Drop identifiers to prevent data leakage in ML model
    drop_patterns = ['src_ip', 'dst_ip', 'src_port', 'dst_port', 'ip', 'port', 
                     'timestamp', 'flow_id', 'protocol', 'socket', 'unnamed', 'simillarhttp']
    cols_to_drop = [c for c in df_chunk.columns if any(p == c for p in drop_patterns)] # Exact match to avoid dropping valid features
    # Broaden exact match slightly to ensure safety
    cols_to_drop = [c for c in df_chunk.columns if c in drop_patterns or 'flow_id' in c or 'timestamp' in c]
    cols_to_drop.extend(possible_labels)
    X = df_chunk.drop(columns=cols_to_drop, errors='ignore')
    y_raw = df_chunk[target_col]
    
    # Handle duplicated 'label' columns returning a DataFrame
    if isinstance(y_raw, pd.DataFrame): 
        y_raw = y_raw.iloc[:, -1]
    # CRITICAL FIX for ALFlowLyzer (L7): Label Encoding string payloads instead of deleting them
    for col in X.columns:
        if X[col].dtype == 'object':
            X[col] = X[col].astype(str)
            le = LabelEncoder()
            X[col] = le.fit_transform(X[col])
    # Downcasting for RAM efficiency
    for col in X.select_dtypes(include=['float64']).columns:
        X[col] = X[col].astype('float32')
        
    for col in X.select_dtypes(include=['int64']).columns:
        X[col] = X[col].astype('int32')
    # Core mathematical sanitization: Replace infinity/NaN generated by stateless protocols
    X.replace([np.inf, -np.inf], np.nan, inplace=True)
    X.fillna(0, inplace=True)
    # Target encoding
    if isinstance(y_raw, pd.DataFrame): y_raw = y_raw.iloc[:, -1]
    y_bin = y_raw.astype(str).str.lower().str.contains('benign').astype(int)
    y_bin = 1 - y_bin # Invert: 1 = Attack, 0 = Benign
    return X, y_bin
def load_dataset(filepath, is_al=False):
    """Loads CSV entirely or via reservoir sampling if above SAFE_THRESHOLD."""
    if not os.path.exists(filepath): return None, None
    fsize = os.path.getsize(filepath)
    if fsize < SAFE_THRESHOLD:
        try:
            df = pd.read_csv(filepath, low_memory=False)
            return process_chunk(df, is_al)
        except: return None, None
    else:
        buffer_X, buffer_y = [], []
        total_rows = 0
        try:
            estimated_rows = (fsize / (1024**3)) * 2_000_000
            sample_rate = min(1.0, MAX_ROWS_PER_FILE / estimated_rows) if estimated_rows > 0 else 0.5
            for chunk in pd.read_csv(filepath, chunksize=CHUNK_SIZE, low_memory=False):
                X_c, y_c = process_chunk(chunk, is_al)
                if X_c is None: continue
                if sample_rate < 1.0:
                    n = int(len(X_c) * sample_rate)
                    if n > 0:
                        idx = np.random.choice(X_c.index, n, replace=False)
                        X_c, y_c = X_c.loc[idx], y_c.loc[idx]
                buffer_X.append(X_c); buffer_y.append(y_c)
                total_rows += len(X_c)
                if total_rows > MAX_ROWS_PER_FILE: break
            if not buffer_X: return None, None
            return pd.concat(buffer_X, ignore_index=True), pd.concat(buffer_y, ignore_index=True)
        except: return None, None
def find_file(base_dir, day_folder, keyword):
    """Search helper mapped to CICDDoS2019 directory topology."""
    search_path = os.path.join(base_dir, day_folder) if day_folder else base_dir
    if not os.path.exists(search_path): return None
    keyword = keyword.lower().replace('drdos_', '')
    for root, _, files in os.walk(search_path):
        for f in files:
            if not f.endswith('.csv') or 'semlabel' in f.lower(): continue
            if keyword == 'udp' and 'lag' in f.lower(): continue
            if keyword in f.lower(): return os.path.join(root, f)
    return None
def run_analysis():
    ml_results_db = []
    print("=== INICIANDO ANÁLISE ML UNIFICADA & EXTRAÇÃO DE FEATURES ===")
    print("Objetivo: Padronizar as Top 10 Features uniformemente para CIC, NTL e AL.\n")
    
    for attack in ATTACK_KEYWORDS:
        print(f"\n=====================================")
        print(f">>> CENÁRIO: {attack}")
        print(f"=====================================")
        
        for ext_name, ext_root in DIRS.items():
            gc.collect()
            is_al = (ext_name == 'AL')
            # Build Training Path (Often 01-12 Folder, but fallback to root if AL doesn't have days)
            train_path = find_file(ext_root, '01-12', attack)
            if not train_path: train_path = find_file(ext_root, '', attack)
            
            if not train_path: 
                print(f"    [{ext_name}] AVISO: CSV base para {attack} não encontrado.")
                continue
            # Build Testing Path (03-11 Folder, fallback to root if AL doesn't have days)
            test_path = find_file(ext_root, '03-11', attack)
            if not test_path and is_al: test_path = find_file(ext_root, '', attack) # Use same file for Split if real is not available
            X_train, y_train = load_dataset(train_path, is_al)
            if X_train is None or len(y_train.unique()) < 2: 
                print(f"    [{ext_name}] ERRO: Dados de treino inválidos ou < 2 classes.")
                continue
            if test_path and train_path != test_path:
                # STRATEGY 1: Real Validation (Generalization Test)
                print(f"    [{ext_name}] Executando Validação REAL (Treino Dia 1 -> Teste Dia 2)...")
                rf = RandomForestClassifier(n_estimators=40, n_jobs=-1, random_state=42, class_weight='balanced', max_depth=15)
                rf.fit(X_train, y_train)
                train_cols = X_train.columns
                
                # Fetch Importances Early (From training data)
                importances = rf.feature_importances_
                
                del X_train, y_train; gc.collect()
                X_test, y_test = load_dataset(test_path, is_al)
                if X_test is None: continue
                # Pad missing features with 0 to match dimensions
                for c in (set(train_cols) - set(X_test.columns)): X_test[c] = 0
                X_test = X_test[train_cols] # Enforce order
                y_pred = rf.predict(X_test)
                
            else:
                # STRATEGY 2: Statistical Split (70/30) - For single-day captures
                print(f"    [{ext_name}] Executando Validação SPLIT (70/30)...")
                X_train_split, X_test_split, y_train_split, y_test_split = train_test_split(X_train, y_train, test_size=0.3, random_state=42, stratify=y_train)
                rf = RandomForestClassifier(n_estimators=40, n_jobs=-1, random_state=42, class_weight='balanced', max_depth=15)
                rf.fit(X_train_split, y_train_split)
                y_pred = rf.predict(X_test_split)
                y_test = y_test_split
                
                importances = rf.feature_importances_
                train_cols = X_train.columns
            # Add new metrics
            f1 = f1_score(y_test, y_pred, average='weighted')
            prec = precision_score(y_test, y_pred, average='weighted', zero_division=0)
            rec = recall_score(y_test, y_pred, average='weighted', zero_division=0)
            print(f"    ✅ F1-Score: {f1:.4f} | Precision: {prec:.4f} | Recall: {rec:.4f}")
            
            ml_results_db.append({
                'Extractor': ext_name,
                'Attack': attack,
                'F1-Score': f1,
                'Precision': prec,
                'Recall': rec
            })
            
            # Print EXACT Top 10 Features formatted for LaTeX table, including weight (mean importance) and std
            importances_std = np.std([tree.feature_importances_ for tree in rf.estimators_], axis=0)
            indices = np.argsort(importances)[::-1]
            top_10 = []
            for i in range(min(10, len(indices))):
                idx = indices[i]
                feat = train_cols[idx]
                mean_w = importances[idx]
                std_w = importances_std[idx]
                top_10.append(f"{feat} ({mean_w:.3f}±{std_w:.3f})")
                
            tex_str = ", ".join(top_10).replace('_', '\\_')
            print(f"    📋 LINHA LATEX: \n    {attack} & {ext_name} & {'Real' if test_path and train_path != test_path else 'Split'} & {prec:.4f} & {rec:.4f} & {f1:.4f} & \\scriptsize{{{tex_str}}} \\\\ \\hline\n")
            del rf, y_pred; gc.collect()
            
    df_ml = pd.DataFrame(ml_results_db)
    df_ml.to_csv(os.path.join(OUTPUT_DIR, 'ml_metrics.csv'), index=False, float_format='%.6f')
    print("\n[+] Exportação Concluída: ml_metrics.csv dinamicamente povoado.")

if __name__ == "__main__":
    run_analysis()
